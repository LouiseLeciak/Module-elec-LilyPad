#include <avr/io.h>
#include "spi_lib.h"

// ─── SPI ---------------------------------------------------------------------

void spi_init(void) {
    DDRB |= (1 << PB3) | (1 << PB5) | (1 << PB2) | (1 << PB1) | (1 << PB0);
    SPCR = (1 << SPE) | (1 << MSTR) | (1 << SPR0);
}

